package main

import (
	"bufio"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// generatedHeader is the marker Speakeasy puts at the top of files it owns.
// We only flag TODO markers in files carrying this header — hand-written
// internal/sdk/ files (token_source.go, login.go, extra_sdk_options.go) etc.
// can carry whatever TODOs the author wants.
const generatedHeader = "Code generated by Speakeasy"

// scanRoots are directories under which we recursively scan generated .go
// files. We skip vendor/, node_modules/, .git/, etc. by enumerating the
// directories we care about rather than excluding ones we don't.
var scanRoots = []string{
	"internal/provider",
	"internal/sdk",
}

// runTodoMarkers scans every Speakeasy-generated .go file for `// TODO`
// comments. Generated code shouldn't ship with author-stub placeholders —
// finding them means the upstream generator emitted incomplete output,
// either due to a regression or due to spec annotations Speakeasy doesn't
// know how to handle.
//
// The check is cheap: read each file once, look for the generated header
// in the first ~10 lines, then grep TODO across the rest. Comparable in
// cost to a single `grep -r`.
func runTodoMarkers() (int, error) {
	type finding struct {
		file string
		line int
		text string
	}
	var findings []finding

	for _, root := range scanRoots {
		err := filepath.WalkDir(root, func(path string, d os.DirEntry, err error) error {
			if err != nil {
				return err
			}
			if d.IsDir() {
				return nil
			}
			if !strings.HasSuffix(path, ".go") {
				return nil
			}
			if strings.HasSuffix(path, "_test.go") {
				return nil
			}
			ff, err := scanFile(path)
			if err != nil {
				return err
			}
			for _, f := range ff {
				findings = append(findings, finding{file: path, line: f.line, text: f.text})
			}
			return nil
		})
		if err != nil {
			if os.IsNotExist(err) {
				continue
			}
			return 0, err
		}
	}

	if len(findings) == 0 {
		return 0, nil
	}

	sort.Slice(findings, func(i, j int) bool {
		if findings[i].file != findings[j].file {
			return findings[i].file < findings[j].file
		}
		return findings[i].line < findings[j].line
	})

	w := os.Stderr
	pln(w)
	pf(w, "WARNING [todo-markers]: %d // TODO marker(s) in Speakeasy-generated files.\n", len(findings))
	pln(w, "         Generated code shouldn't ship with author-stub placeholders. Each one")
	pln(w, "         indicates either an upstream regression or a spec annotation the")
	pln(w, "         generator can't handle. Consider filing upstream and capturing the fix")
	pln(w, "         as a patches/*.patch entry.")
	pln(w)
	for _, f := range findings {
		pf(w, "    %s:%d: %s\n", f.file, f.line, f.text)
	}
	pln(w)
	return len(findings), nil
}

type todoFinding struct {
	line int
	text string
}

// scanFile returns TODO markers if the file is Speakeasy-generated, or nil
// if it's hand-written. Reads the file once, top-to-bottom.
func scanFile(path string) ([]todoFinding, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	scanner.Buffer(make([]byte, 1024*1024), 1024*1024)

	// Look for the generated header in the first N lines. Speakeasy typically
	// emits it at line 1 of every file, but we don't assume that.
	const headerWindow = 20
	headerSeen := false
	headerLines := 0

	var findings []todoFinding
	lineNo := 0
	for scanner.Scan() {
		lineNo++
		line := scanner.Text()
		if !headerSeen {
			if strings.Contains(line, generatedHeader) {
				headerSeen = true
			} else {
				headerLines++
				if headerLines >= headerWindow {
					return nil, nil
				}
				continue
			}
		}
		// We're past the header check; look for TODO markers.
		if idx := strings.Index(line, "// TODO"); idx >= 0 {
			findings = append(findings, todoFinding{line: lineNo, text: strings.TrimSpace(line[idx:])})
		}
	}
	if err := scanner.Err(); err != nil {
		return nil, err
	}
	return findings, nil
}
